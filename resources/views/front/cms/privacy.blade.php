        <p>To accurately understand our clients' use of our services, optimise our web content for you and serve you with the most relevant content, we use Skyquest Technology Group and other companies' cookies, web beacons and embedded hyperlinks.</p>
        <p class="p-24">What are cookies & how do we use them?</p>
        <p>When you visit a website, small text files are placed upon your device and can be read by the website owner. Some are temporary and are deleted when you shut the web browser, some remain on your device for longer or until deleted.</p>
        <p>Cookies serve a variety of purposes inclusive of browser identification, remembering you and your choices, personalisation of the experience, website performance and usage analytics.</p>
        <p>To change your cookie preferences see the "Respecting Your Privacy Preferences" section of this privacy statement.</p>
        <p class="p-24">What are web beacons & how do we use them?</p>
        <p>Web beacons (usually in combination with cookies) is an embedded image which can compile information about your website usage and your interaction with email or other communications, to measure performance, understand usage and to provide content.</p>
        <p>For instance, we may include web beacons in our promotional email messages or newsletters to determine whether our messages have been opened or acted upon and whether our mailing tools are working correctly.</p>
        <p>To change you web beacon preferences see the "Respecting Your Privacy Preferences" section of this privacy statement.</p>
